export const generatePlaceholderDataUrl = (width: number, height: number, text: string = '') => {
  return `data:image/svg+xml;charset=UTF-8,${encodeURIComponent(
    `<svg width="${width}" height="${height}" xmlns="http://www.w3.org/2000/svg">
      <rect x="0" y="0" width="${width}" height="${height}" fill="#f3f4f6"/>
      <text x="50%" y="50%" font-family="Arial" font-size="16" fill="#9ca3af" text-anchor="middle" dy=".3em">${text}</text>
    </svg>`
  )}`
}
